package com.mydomain.smartcrop.operation.statistics;

public class Singularities {
    
    private int x0;
    private int x1;
    
    public Singularities(int x0, int x1) {
        this.x0 = x0;
        this.x1= x1;
    }

    /**
     * @return the x0
     */
    public int getX0() {
        return x0;
    }

    /**
     * @return the x1
     */
    public int getX1() {
        return x1;
    }
}
